# MAIN SEED FUNCTION
# ==========================================

def seed_recipes():
    print("=" * 70)
    print("🍳 BẮT ĐẦU NẠP DỮ LIỆU RECIPES")
    print("-" * 70)

    success = 0
    skip = 0
    error = 0

    for data in RECIPES_DATA:
        try:
            exists = db.query(Recipe).filter(
                or_(
                    Recipe.name_vi == data["name_vi"],
                    Recipe.name_en == data["name_en"]
                )
            ).first()

            if exists:
                print(f"⏭️  Bỏ qua: {data['name_vi']} (Đã tồn tại)")
                skip += 1
                continue

            recipe = Recipe(
                name_vi=data["name_vi"],
                name_en=data["name_en"],
                description=data["description"],
                category=data["category"],
                cuisine_type=data["cuisine_type"],
                prep_time_minutes=data["prep_time_minutes"],
                cook_time_minutes=data["cook_time_minutes"],
                difficulty_level=data["difficulty_level"],
                instructions=data["instructions"],
                instructions_steps=data["instructions_steps"],
                calories_per_serving=Decimal(str(data["calories_per_serving"])),
                protein_per_serving=Decimal(str(data["protein_per_serving"])),
                carbs_per_serving=Decimal(str(data["carbs_per_serving"])),
                fat_per_serving=Decimal(str(data["fat_per_serving"])),
                fiber_per_serving=Decimal(str(data["fiber_per_serving"])),
                tags=data["tags"],
                source="admin_seed_v1.0",
                is_verified=True,
                is_public=True,
                is_deleted=False,
            )

            db.add(recipe)
            db.commit()
            success += 1
            print(f"✅ Đã nạp: {recipe.name_vi}")

        except Exception as e:
            db.rollback()
            print(f"❌ Lỗi tại recipe {data.get('name_vi')}: {str(e)}")
            error += 1

    print("-" * 70)
    print(f"📊 TỔNG KẾT: Thành công {success} | Bỏ qua {skip} | Lỗi {error}")
    print("=" * 70)

# ==========================================
# ENTRY POINT
# ==========================================

if __name__ == "__main__":
    seed_recipes()
    db.close()
